public class exchangeVariable {
    public static void main(String[] args) {
        
        int a, b, x;
        a = 15;
        b = 27;
        System.out.println("Before swapping : a, b = "+a+", "+ + b);
        x = a;
        a = b;
        b = x;   
        System.out.println("After swapping : a, b = "+a+", "+ + b);
      }
    }